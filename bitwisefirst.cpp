#include<iostream>
using namespace std;
int main()
{
//!..Bitwise AND(&)... 
    int x=3;
    int y=6;
    cout<<(x & y)<<"\n";
//!..Bitwise OR(|)... 
    int a=3;
    int b=6;
    cout<<(a | b)<<endl;
//!..Bitwise XOR(^)... 
    int c=3;
    int d=6;
    cout<<(c ^ d)<<"\n";
    
}