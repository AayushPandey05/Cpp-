//TODO: USING FLOATING POINT IN LITERAL.....
#include<iostream>
using namespace std;
int main()
{
    float a = 10.5f; //!float...
    double b = 10.515; //!double...
    float c = 2.1e15f; //! 2.1 x 10^15...
    double d = 200.1e-80; //! 200.1 x 10^-80 or 200.1 x 10^-78...
    double f = 0x1a.1p2; //! 26.0625 x 2^2 => 104.25...
    cout<<a <<"\n"<<b <<"\n"<<c <<"\n"<<d <<"\n"<<f;
    return 0;
}